package njust.edu.wuziqi;

public class Location {
	private int x_site;
	private int y_site;

	public Location(int x, int y) {
		this.x_site = x;
		this.y_site = y;
	}

	public int getX_site() {
		return x_site;
	}

	public void setX_site(int x_site) {
		this.x_site = x_site;
	}

	public int getY_site() {
		return y_site;
	}

	public void setY_site(int y_site) {
		this.y_site = y_site;
	}

}
